namespace Forest
{
    /// <summary>Specifies how binary tree should be traversed. </summary>
    public enum BinaryTraversalMethod
    {
        /// <summary>Specifies that the binary tree should be traversed pre order.</summary>
        PreOrder,
        /// <summary>Specifies that the binary tree should be traversed in order.</summary>
        InOrder,
        /// <summary>Specifies that the binary tree should be traversed post order.</summary>
        PostOrder
    }
}
